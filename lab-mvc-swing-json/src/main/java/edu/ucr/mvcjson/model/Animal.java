package edu.ucr.mvcjson.model;

public class Animal {

    private String name;
    private String species;
    private int age;
    private String ownerPhone;

    public Animal(String name, String species, int age, String ownerPhone) {
        this.name = name;
        this.species = species;
        this.age = age;
        this.ownerPhone = ownerPhone;
    }
}
